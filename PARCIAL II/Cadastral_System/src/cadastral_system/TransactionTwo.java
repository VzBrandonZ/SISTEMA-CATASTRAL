    private void btntransaccionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntransaccionalActionPerformed
        ConexionPostgreSQL con = new ConexionPostgreSQL();
        CallableStatement cs;
        try {
            Connection conexion =con.Conectar();
            cs=conexion.prepareCall("call transactionTwo(?,?,?,?)");
            SimpleDateFormat formato=new SimpleDateFormat("yyyy/MM/dd");
            
            cs.setInt(1,Integer.parseInt(txtIdfecha.getText()));
            
            if("".equals(txtFechasolicitud.getText())){
                cs.setDate(2, null);
            }else{
                Date fecha=formato.parse(txtFechasolicitud.getText());
                java.sql.Date date=new java.sql.Date(fecha.getTime());
                cs.setDate(2, date);
            }
            
            if("".equals(txtFechainicial.getText())){
                cs.setDate(3, null);
            }else{
                Date fecha2 = formato.parse(txtFechainicial.getText());
                java.sql.Date date2 = new java.sql.Date(fecha2.getTime());
                cs.setDate(3, date2);
            }
            
            
            if("".equals(txtFechafinal.getText())){
                cs.setDate(4, null);
            }else{
                Date fecha3 = formato.parse(txtFechafinal.getText());
                java.sql.Date date3 = new java.sql.Date(fecha3.getTime());
                cs.setDate(4, date3);
            }
                        

            cs.execute();
            cs.close();
            conexion.close();
            JOptionPane.showMessageDialog(null, "¡Transaccion faliida!", "Error", JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(null, "Transaccion ejecutada correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al conectar "+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btntransaccionalActionPerformed

