    private void btnconsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultaActionPerformed
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Id solicitud");
        modelo.addColumn("Id catastral");
        modelo.addColumn("Id servicio");
        modelo.addColumn("Id propietario");
        modelo.addColumn("id tecnico");
        modelo.addColumn("Estado");
        modelo.addColumn("Id fecha");
        modelo.addColumn("Id descripción");
        modelo.addColumn("Monto");

        tb.setModel(modelo);
        String datos[]= new String[10];
        ConexionPostgreSQL con = new ConexionPostgreSQL();
        try {
            Connection conexion =con.Conectar();
            java.sql.Statement st = conexion.createStatement();
            String sql="select * from solicitud order by id_solicitud asc";
            ResultSet rs=st.executeQuery(sql);
            while (rs.next())
            {
                datos[0]=rs.getString("id_solicitud");
                datos[1]=rs.getString("id_catastral");
                datos[2]=rs.getString("id_serv_tecnico");
                datos[3]=rs.getString("id_propietario");
                datos[4]=rs.getString("id_tecnico");
                datos[5]=rs.getString("estado_solicitud");
                datos[6]=rs.getString("id_fecha");
                datos[7]=rs.getString("id_descripcion");
                datos[8]=rs.getString("monto_pagar");

                modelo.addRow(datos);
            }
            rs.close();
            conexion.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema de busqueda","Error buscqueda",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnconsultaActionPerformed

