    private void btntransaccionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntransaccionalActionPerformed
        ConexionPostgreSQL con = new ConexionPostgreSQL();
        CallableStatement cs;
        try {
            Connection conexion =con.Conectar();
            cs=conexion.prepareCall("call transactionOne(?,?,?,?,?,?,?,?)");
            //SimpleDateFormat formato=new SimpleDateFormat("dd/MM/yyyy");
            //Date fecha=formato.parse(txtIdpredio.getText());
            //java.sql.Date date=new java.sql.Date(fecha.getTime());
            cs.setInt(1,Integer.parseInt(txtIdlote.getText()));
            cs.setInt(2,Integer.parseInt(txtIdpredio.getText()));
            cs.setInt(3, Integer.parseInt(txtCantidadlote.getText()));
            cs.setString(4, (txtArealote.getText()));
            cs.setString(5, (txtTipolote.getText()));
            cs.setString(6, (txtNombrepredio.getText()));
            cs.setString(7, (txtSitioreferencial.getText()));
            cs.setString(8, (txtNumeroTitulo.getText()));

            cs.execute();
            cs.close();
            conexion.close();
            JOptionPane.showMessageDialog(null, "Transaccion ejecutada correctamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al conectar "+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btntransaccionalActionPerformed

