        modelo.addColumn("Sitio referencial");
        modelo.addColumn("estado");
        modelo.addColumn("numero de titulo");
        tb.setModel(modelo);
        String datos[]= new String[20];
        ConexionPostgreSQL con = new ConexionPostgreSQL();
        try {
            Connection conexion =con.Conectar();
            java.sql.Statement st = conexion.createStatement();
            String sql="select * from predio order by id_predio asc";
            ResultSet rs=st.executeQuery(sql);
            while (rs.next())
            {
                datos[0]=rs.getString("id_predio");
                datos[1]=rs.getString("id_zona");
                datos[2]=rs.getString("id_provincia");
                datos[3]=rs.getString("id_canton");
                datos[4]=rs.getString("id_parroquia");
                datos[5]=rs.getString("id_tipo_via");
                datos[6]=rs.getString("id_rodadura");
                datos[7]=rs.getString("nombre_predio");
                datos[8]=rs.getString("med_frente");
                datos[9]=rs.getString("med_fondo");
                datos[10]=rs.getString("med_izquierda");
                datos[11]=rs.getString("med_derecha");
                datos[12]=rs.getString("med_area");
                datos[13]=rs.getString("calle_principal_pre");
                datos[14]=rs.getString("calle_secundaria_pre");
                datos[15]=rs.getString("sitio_referencia");
                datos[16]=rs.getString("estado_predio");
                datos[17]=rs.getString("num_titulo_predio");

                modelo.addRow(datos);
            }
            rs.close();
            conexion.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema de busqueda: "+e,"Error buscqueda",JOptionPane.ERROR_MESSAGE);
        }
