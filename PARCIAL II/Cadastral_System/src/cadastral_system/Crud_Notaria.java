    private void btnrealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrealizarActionPerformed
        switch (combo.getSelectedItem().toString()) {
            case "Insertar":
            objcrud.insertarNotaria(txtNumero.getText(),txtNombre.getText(), txtRepresentante.getText(), txtDireccion.getText(), txtSitio_referencia_not.getText(), txtTelefono.getText());
            break;
            case "Consulta Especifica":
            btneliminar.setVisible(true);
            btnmodificar.setVisible(true);
            txtNombre.setEnabled(true);
            txtRepresentante.setEnabled(true);
            txtDireccion.setEnabled(true);
            txtSitio_referencia_not.setEnabled(true);
            txtTelefono.setEnabled(true);
            objcrud.consultaIdNotaria(txtNumero.getText());
            txtNumero.setText(var.getId_not());
            txtNombre.setText(var.getNombre_not());
            txtRepresentante.setText(var.getRepresentante_not());
            txtDireccion.setText(var.getCalle_principal_not());
            txtSitio_referencia_not.setText(var.getSitio_referencia_not());
            txtTelefono.setText(var.getTelefono_not());
            break;
            case "Consulta General":
            DefaultTableModel modelo=new DefaultTableModel();
            modelo.addColumn("Numero");
            modelo.addColumn("Nombres");
            modelo.addColumn("Representantes");
            modelo.addColumn("Direccion");
            modelo.addColumn("Sitio Referencial");
            modelo.addColumn("Telefono");
            tb.setModel(modelo);
            String datos[]= new String[10];
            ConexionPostgreSQL con = new ConexionPostgreSQL();
            try {
                Connection conexion =con.Conectar();
                java.sql.Statement st = conexion.createStatement();
                String sql="select * from notaria";
                ResultSet rs=st.executeQuery(sql);
                while (rs.next())
                {
                    datos[0]=rs.getString("id_not");
                    datos[1]=rs.getString("nombre_not");
                    datos[2]=rs.getString("representante_not");
                    datos[3]=rs.getString("calle_principal_not");
                    datos[4]=rs.getString("sitio_referencia_not");
                    datos[5]=rs.getString("telefono_not");
                    modelo.addRow(datos);
                    
                }
                rs.close();
                conexion.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error en el sistema de busqueda","Error buscqueda",JOptionPane.ERROR_MESSAGE);
            }
            break;
            default:
            throw new AssertionError();
        }
    }//GEN-LAST:event_btnrealizarActionPerformed

